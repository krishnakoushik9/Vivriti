# DOCX EXPORT
# ─────────────────────────────────────────────────────────────────
def export_cam_to_docx(
    cam_markdown: str,
    metadata: Dict[str, Any],
    output_path: Optional[str] = None,
) -> str:
    """
    Convert CAM markdown to a professionally formatted DOCX document.
    """
    try:
        from docx import Document
        from docx.shared import Inches, Pt, Cm, RGBColor
        from docx.enum.text import WD_ALIGN_PARAGRAPH
    except ImportError:
        logger.error("python-docx not installed.")
        raise ImportError("python-docx is required for DOCX export")

    doc = Document()
    
    # Page setup
    section = doc.sections[0]
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)
    section.left_margin = Cm(2.5)
    section.right_margin = Cm(2.5)

    # Title
    title = doc.add_heading("Credit Appraisal Memo", level=0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER

    # Subtitle / metadata
    company_name = metadata.get("company_name", "Unknown Company")
    application_id = metadata.get("application_id", "N/A")

    subtitle = doc.add_paragraph()
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = subtitle.add_run(f"{company_name}\n")
    run.font.size = Pt(14)
    run.font.color.rgb = RGBColor(0x0B, 0x1D, 0x51)
    run.bold = True

    run2 = subtitle.add_run(f"Application ID: {application_id}")
    run2.font.size = Pt(10)
    run2.font.color.rgb = RGBColor(0x66, 0x66, 0x66)

    doc.add_paragraph()  # Spacer

    # Notice
    conf_para = doc.add_paragraph()
    conf_run = conf_para.add_run(
        "CONFIDENTIAL — For internal use only. This document contains proprietary "
        "credit analysis generated by the IntelliCredit AI Decision Engine."
    )
    conf_run.font.size = Pt(8)
    conf_run.font.italic = True
    conf_run.font.color.rgb = RGBColor(0xCC, 0x00, 0x00)

    doc.add_paragraph()

    # Parse markdown (simplified)
    lines = cam_markdown.split("\n")
    for line in lines:
        line = line.rstrip()
        if line.startswith("### "):
            doc.add_heading(line[4:].strip(), level=3)
        elif line.startswith("## "):
            doc.add_heading(line[3:].strip(), level=2)
        elif line.startswith("# "):
            doc.add_heading(line[2:].strip(), level=1)
        elif line.startswith("---"):
            doc.add_paragraph("─" * 60)
        elif line.startswith("- ") or line.startswith("* "):
            p = doc.add_paragraph(style="List Bullet")
            p.add_run(line[2:].strip().replace("**", ""))
        elif line.strip() and not line.startswith("|"):
            p = doc.add_paragraph()
            p.add_run(line.replace("**", ""))

    # Footer
    doc.add_paragraph()
    footer = doc.add_paragraph()
    f_run = footer.add_run(
        "DISCLAIMER: This CAM is generated by the IntelliCredit AI engine. "
        "All ML risk scores are deterministic and transparent. "
        "Final lending decisions require human review per RBI DL Guidelines 6(a)."
    )
    f_run.font.size = Pt(7)
    f_run.font.italic = True
    f_run.font.color.rgb = RGBColor(0x99, 0x99, 0x99)

    if output_path is None:
        os.makedirs("./exports", exist_ok=True)
        output_path = f"./exports/CAM_{application_id}.docx"

    doc.save(output_path)
    return output_path

