def export_cam_to_pdf(
    cam_markdown: str,
    metadata: Dict[str, Any],
    output_path: Optional[str] = None,
) -> str:
    """
    Convert CAM markdown to a formatted PDF with full professional layout.
    """
    try:
        from fpdf import FPDF
    except ImportError:
        logger.error("fpdf2 not installed.")
        raise ImportError("fpdf2 is required for PDF export")

    company_name = metadata.get("company_name", "Unknown Company")
    application_id = metadata.get("application_id", "N/A")

    class CAMReport(FPDF):
        def header(self):
            if self.page_no() > 1:
                self.set_font("Helvetica", "B", 8)
                self.set_text_color(150, 150, 150)
                usable_w = self.w - self.l_margin - self.r_margin
                self.cell(usable_w, 10, _pdf_safe(f"CONFIDENTIAL | IntelliCredit CAM | {application_id}"), align="R", ln=1)
                self.ln(5)
            
        def footer(self):
            self.set_y(-15)
            self.set_font("Helvetica", "I", 8)
            self.set_text_color(150, 150, 150)
            self.cell(0, 10, f"Page {self.page_no()}/{{nb}}", align="C")

    pdf = CAMReport(orientation='P', unit='mm', format='A4')
    pdf.alias_nb_pages()
    pdf.set_auto_page_break(auto=True, margin=20)
    pdf.set_left_margin(25)
    pdf.set_right_margin(25)
    pdf.set_top_margin(20)
    
    # ── COVER PAGE ──
    pdf.add_page()
    usable_w = pdf.w - pdf.l_margin - pdf.r_margin
    
    pdf.ln(40)
    pdf.set_font("Helvetica", "B", 24)
    pdf.set_text_color(11, 29, 81) # Navy #0B1D51
    pdf.multi_cell(usable_w, 12, "Credit Appraisal Memorandum", align="C")
    
    pdf.ln(10)
    pdf.set_font("Helvetica", "B", 16)
    pdf.set_text_color(50, 50, 50)
    pdf.multi_cell(usable_w, 10, _pdf_safe(company_name), align="C")
    
    pdf.ln(5)
    pdf.set_font("Helvetica", "", 12)
    pdf.set_text_color(100, 100, 100)
    pdf.cell(usable_w, 8, _pdf_safe(f"Application ID: {application_id}"), align="C", ln=1)
    
    pdf.ln(20)
    pdf.set_draw_color(11, 29, 81)
    pdf.set_line_width(0.5)
    pdf.line(pdf.l_margin, pdf.get_y(), pdf.w - pdf.r_margin, pdf.get_y())
    
    pdf.ln(10)
    pdf.set_font("Helvetica", "B", 10)
    pdf.set_text_color(0, 0, 0)
    pdf.cell(usable_w, 6, "Prepared by: IntelliCredit AI Engine", align="C", ln=1)
    pdf.cell(usable_w, 6, "For: Credit Committee Review", align="C", ln=1)
    pdf.ln(10)
    
    pdf.set_font("Helvetica", "I", 9)
    pdf.set_text_color(180, 0, 0) # Muted red for notice
    pdf.multi_cell(usable_w, 5, "CONFIDENTIALITY NOTICE: This document contains proprietary credit analysis generated by the IntelliCredit AI Decision Engine. For internal use by Vivriti Capital only. Unauthorized distribution is strictly prohibited.", align="C")
    
    pdf.ln(10)
    pdf.set_draw_color(11, 29, 81)
    pdf.line(pdf.l_margin, pdf.get_y(), pdf.w - pdf.r_margin, pdf.get_y())
    
    # ── CONTENT PAGES ──
    pdf.add_page()
    
    def render_markdown_table(pdf, table_lines, usable_w):
        if not table_lines: return
        rows = []
        for line in table_lines:
            if line.strip().startswith("|") and "---" not in line:
                cells = [c.strip() for c in line.strip("|").split("|")]
                rows.append(cells)
        if not rows: return
        
        col_count = max(len(r) for r in rows)
        col_w = usable_w / col_count
        
        for i, row in enumerate(rows):
            # Check for page break
            if pdf.get_y() > 260:
                pdf.add_page()
                
            if i == 0:
                pdf.set_font("Helvetica", "B", 9)
                pdf.set_fill_color(240, 240, 240)
                fill = True
            else:
                pdf.set_font("Helvetica", "", 9)
                pdf.set_fill_color(255, 255, 255)
                fill = i % 2 == 0
            
            # Ensure row has enough cells
            row_cells = row + [""] * (col_count - len(row))
            
            for cell in row_cells:
                pdf.cell(col_w, 8, _pdf_safe(cell[:50]), border=1, fill=fill)
            pdf.ln()
        pdf.ln(5)

    lines = cam_markdown.split("\n")
    i = 0
    while i < len(lines):
        line = lines[i].rstrip()
        
        # Table detection
        if line.strip().startswith("|"):
            table_lines = []
            while i < len(lines) and (lines[i].strip().startswith("|") or lines[i].strip() == ""):
                if lines[i].strip().startswith("|"):
                    table_lines.append(lines[i])
                i += 1
            render_markdown_table(pdf, table_lines, usable_w)
            continue

        clean = re.sub(r"\*\*(.*?)\*\*", r"\1", line)
        clean = re.sub(r"\*(.*?)\*", r"\1", clean)
        clean = _pdf_safe(clean)

        if line.startswith("# "):
            pdf.ln(5)
            pdf.set_font("Helvetica", "B", 16)
            pdf.set_text_color(11, 29, 81) # Navy #0B1D51
            pdf.multi_cell(usable_w, 8, clean[2:].strip())
            pdf.ln(2)
        elif line.startswith("## "):
            pdf.ln(4)
            pdf.set_font("Helvetica", "B", 13)
            pdf.set_text_color(11, 29, 81) # Navy #0B1D51
